def test_email_config(username, password):
    """Test the Gmail SMTP configuration"""
    print("\nðŸ§ª Testing Gmail SMTP configuration...")
    
    try:
        import smtplib
        
        # Test connection
        server = smtplib.SMTP('smtp.gmail.com', 587)
        server.starttls()
        server.login(username, password)
        server.quit()
        
        print("âœ… Gmail SMTP connection successful!")
        print("   Your email configuration is working correctly.")
        
    except ImportError:
        print("âŒ smtplib not available (this is unusual)")
    except smtplib.SMTPAuthenticationError:
        print("âŒ Authentication failed!")
        print("   - Check your Gmail address")
        print("   - Verify you're using App Password (not regular password)")
        print("   - Ensure 2FA is enabled on Gmail")
    except smtplib.SMTPConnectError:
        print("âŒ Connection failed!")
        print("   - Check your internet connection")
        print("   - Verify SMTP server: smtp.gmail.com:587")
    except Exception as e:
        print(f"âŒ Test failed: {e}")

def show_status():
    """Show current email configuration status"""
    print("ðŸ“Š Current Email Configuration Status")
    print("=" * 40)
    
    env_file = Path(".env")
    if not env_file.exists():
        print("âŒ No .env file found")
        print("   Run: python3 setup_gmail.py")
        return
    
    # Check environment variables
    required_vars = [
        'SMTP_USERNAME', 'SMTP_PASSWORD', 'FROM_EMAIL', 
        'SMTP_SERVER', 'SMTP_PORT', 'EMAIL_SECRET_KEY'
    ]
    
    print("ðŸ“ .env file exists: âœ…")
    
    # Load and check variables
    env_vars = {}
    with open(env_file) as f:
        for line in f:
            line = line.strip()
            if line and not line.startswith('#') and '=' in line:
                key, value = line.split('=', 1)
                env_vars[key] = value
    
    print("\nðŸ” Configuration check:")
    for var in required_vars:
        if var in env_vars and env_vars[var]:
            if var == 'SMTP_PASSWORD':
                print(f"   {var}: âœ… (hidden)")
            else:
                print(f"   {var}: âœ… {env_vars[var]}")
        else:
            print(f"   {var}: âŒ Missing or empty")
    
    if all(var in env_vars and env_vars[var] for var in required_vars):
        print("\nâœ… Configuration looks complete!")
    else:
        print("\nâš ï¸  Configuration incomplete - run setup again")

if __name__ == "__main__":
    import sys
    
